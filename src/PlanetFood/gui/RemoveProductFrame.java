    private void btnRemoveProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveProductActionPerformed
         try
        {
            int ans=JOptionPane.showConfirmDialog(null,"ARE YOU SURE?","DELETE PRODUCT!",JOptionPane.YES_NO_OPTION);
            if(ans==JOptionPane.NO_OPTION)
                return;
            String productId=(String)jcbProductId.getSelectedItem(); 
            if(ProductDao.removeProduct(productId))
            {
                JOptionPane.showMessageDialog(null,"Record deleted Successfully ","Deleted!",JOptionPane.INFORMATION_MESSAGE);
                jcbProductId.removeAllItems();
            }
            else
                JOptionPane.showMessageDialog(null,"could not delete product","Input Error!",JOptionPane.ERROR_MESSAGE); 
        }
         catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB"+ex,"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace(); 
        }
    }//GEN-LAST:event_btnRemoveProductActionPerformed

