    private void jcbProductCategoryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbProductCategoryActionPerformed
        try
       {
          String catName=jcbProductCategory.getSelectedItem().toString();
          String catId=Categories.get(catName);
       
          productList=ProductDao.getProductsByCategories(catId);
          jcbProductId.removeAllItems();
           for(String proId:productList.keySet())
               jcbProductId.addItem(proId);
       }
       catch(SQLException ex)
               {
                   JOptionPane.showMessageDialog(null, "Error in sql!" + ex, "Input Error", JOptionPane.ERROR_MESSAGE);
                   ex.printStackTrace();
               
               }
              
    }//GEN-LAST:event_jcbProductCategoryActionPerformed

